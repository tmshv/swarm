function createAgentInRandomCoords(width, height) {
    const {x, y} = randomCoord([width, height])

    return createAgent(new Vector(x, y))
}

function createAgent(loc) {
    const a = new Agent()
    a.damp = 0.85
    a.location.set(loc.x, loc.y)
    a.addBehaviour(new TtlBehavior({
        ttl: 1000,
    }))
    a.addBehaviour(new SpreadPheromonesBehaviour({
        pheromones,
    }))
    // a.addBehaviour(new SeekNearestAttractorBehaviour({
    //     accelerate: 0.1,
    //     thresholdDistQuad: 10,
    // }))
    a.addBehaviour(new InteractAgentsBehaviour({
        accelerate: 0.4,
        radius: 100,
        initialInterest: 200,
    }))
    // a.addBehaviour(new RandomWalkBehaviour({
    //     accelerate: 0.25,
    // }))
    a.addBehaviour(new InteractEnvironmentBehaviour({
        accelerate: 0.1
    }))

    a.addBehaviour(new AvoidObstaclesBehavior({
        accelerate: 1,
        predictionDistance: 5,
        radius: 1000,
    }))

    if (!agent) {
        agent = a
    }

    return a
}

function createAttractor({x, y, power}) {
    const id = Id.get('attractor')
    return new Attractor({id, x, y, power})
}

function randomCoord([maxX, maxY]) {
    const x = Math.random() * maxX
    const y = Math.random() * maxY

    return {x, y}
}

